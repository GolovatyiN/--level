-- Per-task free-text «Направление» / тег.
--
-- Намеренно простое TEXT-поле, без отдельной справочной таблицы и без
-- триггеров автодобавления — пользователь пишет значение от руки, оно
-- остаётся локально внутри задачи. Глобальная система тегов (`tags`,
-- `task_tags`, `kpi_tags`) живёт независимо и не затрагивается.
ALTER TABLE public.tasks
  ADD COLUMN IF NOT EXISTS direction_tag TEXT;

